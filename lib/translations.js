export function getLanguageMeta(code) {
  return LANGUAGES.find((l) => l.code === code) || LANGUAGES[0];
}

export function getStoredLanguage() {
  if (typeof window === "undefined") return "en";
  const raw = window.localStorage.getItem(LANGUAGE_STORAGE_KEY) || "en";
  return LANGUAGES.some((l) => l.code === raw) ? raw : "en";
}

export function setStoredLanguage(code) {
  if (typeof window === "undefined") return;
  const safe = LANGUAGES.some((l) => l.code === code) ? code : "en";
  window.localStorage.setItem(LANGUAGE_STORAGE_KEY, safe);
  window.dispatchEvent(new CustomEvent(LANGUAGE_EVENT, { detail: { language: safe } }));
}

export function tFor(language, key) {
  const lang = TRANSLATIONS[language] ? language : "en";
  return TRANSLATIONS[lang]?.[key] || TRANSLATIONS.en[key] || key;
}

